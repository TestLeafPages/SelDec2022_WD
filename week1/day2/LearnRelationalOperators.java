package week1.day2;

public class LearnRelationalOperators {

	public static void main(String[] args) {
//     dataType variable assignment operator value		
		int       num1     =                 10;
		int       num2     =                  5;
		
		System.out.println(num1 > num2);//true
		System.out.println("=================");
		System.out.println(num1 < num2);//false
		System.out.println("=================");
		System.out.println(num1 == num2);//false
		System.out.println("=================");
		System.out.println(num1 != num2);//true
		System.out.println("=================");
		System.out.println(num1 >= num2);//true
		System.out.println("=================");
		System.out.println(num1 <= num2);//false
		System.out.println("=================");
		

	}

}

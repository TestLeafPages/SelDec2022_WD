package week1.day2;

public class PrintOddNumbers {

	public static void main(String[] args) {
		//print odd numbers from 1 to 10
	
		//Declare a for loop
		for (int i = 1; i <=10; i++) {//i=5;5<=10->true
			if (i%2!=0) {//5%2==1->true
				System.out.println(i);//1 3 5
			}
		
			
		}
	
		

	}

}
